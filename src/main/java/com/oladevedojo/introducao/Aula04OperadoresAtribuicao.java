package com.oladevedojo.introducao;

public class Aula04OperadoresAtribuicao {
    public static void main(String[] args) {
        // = += -= *= /= %=
        double bonus = 1800;
        bonus += 1000;
        bonus -= 1000;
        bonus *= 2;
        bonus /= 2;
        bonus %= 2;
        System.out.println(bonus);
    }
}
